package es.uco.pw.clases.material;

/**
 * Enumeración Tipo.
 * Representa los diferentes tipos de materiales que pueden ser utilizados en una pista deportiva.
 */
public enum Tipo {
    
    /**
     * Representa el tipo de material 'pelotas'.
     */
    pelotas,
    
    /**
     * Representa el tipo de material 'canastas'.
     */
    canastas,
    
    /**
     * Representa el tipo de material 'conos'.
     */
    conos,
    
    /**
     * Valor por defecto cuando no se ha asignado ningún tipo de material.
     */
    none 
}
